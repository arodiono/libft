int	ft_atoi(const char *str)
{
	int					i;
	int					f;
	unsigned long int	r;

	r = 0;
	i = 0;
	f = 1;
	while ((str[i] > 8 && str[i] < 14) || str[i] == 32)
		i++;
	if (str[i] == '+')
		i++;
	else if (str[i] == '-')
	{
		f = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		r = (r * 10) + (str[i++] - '0');
		if (r >= 9223372036854775807 && f == 1)
			return (-1);
		else if (r > 9223372036854775807 && f == -1)
			return (0);
	}
	return (r * f);
}
