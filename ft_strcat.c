char	*ft_strcat(char *dst, const char *src)
{
	int i;
	int a;

	i = 0;
	while (dst[i] != '\0')
	{
		i++;
	}
	a = 0;
	while (src[a] != '\0')
	{
		dst[i] = src[a];
		a++;
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
