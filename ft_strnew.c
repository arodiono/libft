#include <string.h>
#include <stdlib.h>

char	*ft_strnew(size_t size)
{
	char *str;

	str = (char*)malloc(size + 1);
	if (str == NULL)
		return (NULL);
	size++;
	while (size-- > 0)
		str[size] = '\0';
	return (str);
}
